class Grafica extends StatefulWidget {
  @override
  _Grafica createState() => _Grafica();
}

String selectedSignal;
String signalValue;
const String SIGNAL_SANA = 'Señal Sana';
const String SIGNAL_ENFERMA = 'Señal Enferma';
const String SIGNAL_SANA_VALUE = 'sana';
const String SIGNAL_ENFERMA_VALUE = 'enferma';
class _Grafica extends State<Grafica> {
  List<String> signals = <String>[SIGNAL_SANA, SIGNAL_ENFERMA];


  @override
  void initState() {
    if(globals.signalSelected==SIGNAL_SANA_VALUE){
      selectedSignal = SIGNAL_SANA;
      signalValue = SIGNAL_SANA_VALUE;
    }else{
      selectedSignal = SIGNAL_ENFERMA;
      signalValue = SIGNAL_ENFERMA_VALUE;
    }

    fillArraySignal();
  }

  Widget listDropDown() {
    return DropdownButton<String>(
        hint: Text("Select item"),
        value: selectedSignal,
        onChanged: (value) {
          setState(() {
            selectedSignal = value;
            signalValue = value == SIGNAL_SANA ? SIGNAL_SANA_VALUE: SIGNAL_ENFERMA_VALUE;
            globals.signalSelected = signalValue;
            arreglo = null;
            fillArraySignal();
          });
        },
        items: signals.map((String signal) {
          return DropdownMenuItem<String>(
            value: signal,
            child: Row(
              children: <Widget>[
                SizedBox(width: 10),
                Text(
                  signal,
                  style: TextStyle(color: Colors.white),
                ),
              ],
            ),
          );
        }).toList(),
      );
  }


  Future<http.Response> downloadSignalArray() {
    return http.get('https://back-ecg.herokuapp.com/api/signals/'+signalValue);
  }

  fillArraySignal() async{
    var response = await downloadSignalArray();
    print(response);
    var responseJson = json.decode(response.body);
    print(responseJson['name']);
    setState(() {
      arreglo = responseJson['signal'];
      print(arreglo);
    });
  }

  @override
  Widget build(BuildContext context) {
    return arreglo==null? Center(
      child: Container(
        height: 50,
        width: 50,
        margin: EdgeInsets.all(5),
        child: CircularProgressIndicator(
          strokeWidth: 2.0,
          valueColor : AlwaysStoppedAnimation(Colors.red),
        ),
      ),
    ):Scaffold(
      body: ListView(children: <Widget>[
        listDropDown(),
          Container(
          width: 400,
          height: 400,
          child: CustomPaint(
                painter: OpenPainter()
            ),
          ),
      ]
    ));
  }
}

